// Utility: Get players for a team filtered by year range
function getPlayersForTeam(teamId, yearFrom, yearTo) {
  return PLAYERS.filter(p => {
    if (p.team !== teamId) return false;
    const [start, end] = p.peak_years;
    // Player was active in the given window
    return start <= yearTo && end >= yearFrom;
  });
}

// Utility: Get a random team
function getRandomTeam() {
  return TEAMS[Math.floor(Math.random() * TEAMS.length)];
}
